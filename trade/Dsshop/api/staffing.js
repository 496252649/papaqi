import Network from '../utils/network.js'
export default {
    getList(data,success,fail) {
		Network.setGetMessage('staffing',data,'加载中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
    },
	createSubmit(data,success,fail) {
		Network.setPostMessage('staffing',data,'处理中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	deleteSubmit(id,success,fail){
		Network.setPostMessage('staffingDelete/' + id,{},'处理中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	}
};
